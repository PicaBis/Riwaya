export function t(key: string, lang: Lang, params?: Record<string, string | number>): string {
  let text = dict[lang]?.[key] || dict.ar[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}

